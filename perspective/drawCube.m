function [ V, T ] = drawCube()
%DRAWCUBE draws a 1x1x1 cube
%   Returns vertices in V
%   T contains what vertices make up the triangles

V = [ ...
    0.5 -0.5 0.5 1; ...
    0.5 -0.5 -0.5 1; ...
    -0.5 -0.5 -0.5 1; ...
    -0.5 -0.5 0.5 1; ...
    0.5 0.5 0.5 1; ...
    0.5 0.5  -0.5 1; ...
    -0.5 0.5 -0.5 1; ...
    -0.5 0.5 0.5 1; ...
    0 0 -0.5 1; ...
    0 0 0.5 1; ...
    -0.5 0 0 1; ...
    0.5 0 0 1; ...
    0 -0.5 0 1; ...
    0 0.5 0 1; ...
    ]';

T = [ ...
    1 2 3; %Bottom face
    1 3 4;
    5 6 7; %Top face
    7 8 5;
    1 5 4; %Front
    5 4 8;
    2 7 6; %Back
    2 3 6;
    4 8 3; %Left
    8 3 7;
    1 5 6; %Right
    1 2 6;
    ]';


end

